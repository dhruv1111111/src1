public class ContainsExample {
    public static void main(String[] args) {
        String s1 = "What about your feelings?";
        System.out.println(s1.contains("What"));
        System.out.println(s1.contains("why"));
        System.out.println(s1.contains("about"));
    }
}


// output :
//    true
//    false
//    true
